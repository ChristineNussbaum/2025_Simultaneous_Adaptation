## Meaning of Variables

#participant                    #individual participant code, generated by the system
#LAge                           #participants' age
#LSex                           #participants' sex: 1= female, 2 = male, 3 = diverse
#LMotherLanguage                #mother tongue, 1 = German, 2 = English, 3 = other
#LStudyWork                     #profession or study subject 
#LHearingKown                   #Hearing impairment? 1 = no, 2 = yes, 3 = don't know
#LHearingKind                   #in case of hearing impairment -> which kind
#LHearingImp                    #in case of hearing impairment -> degree of hearing impairment (1 = no impairment, 4 = extreme impairment)
#afterExp1AllSoundsPlayed_1     #were all sounds played after the experiment? 1 = yes, 2 = no, 3 = don't know
#InstructionsClear              #were all instructions clear in the experiment?            
#Eval1 - Eval8                  #8 questions about self-rated voice abilities and commitment to the Experiment  (see below)
#Strategy                       #did participants use a particular strategy during the task? (voluntary free-text field)
#Anmerkungen                    #any remarks about the experiment? (voluntary free-text field)
#TIME_total                     #total time of the experiment
#VPN_Code                       #individual participant code, self-generated



#------------------------------------------------------------------------------------------------------------------#
#                                           Sample Demographics                                                    #
#------------------------------------------------------------------------------------------------------------------#

#general demographics
Age <- mySummary(S3, LAge)
range <- S3 %>% summarise(range(LAge))
sex <- table(S3$LSex)
language <- table(S3$LMotherLanguage)
LStudyWork <- table(S3$LStudyWork)
LHearingKown <- table(S3$LHearingKown)
LHearingKind <- table(S3$LHearingKind)
LHearingImp <- table(S3$LHearingImp)
duration <- S3 %>% summarise(Tmean = mean(TIME_total, na.rm= TRUE),
                            min = min(TIME_total, na.rm= TRUE),
                            max = max(TIME_total, na.rm= TRUE))
capture.output(as.matrix(Age), as.matrix(range), sex, language, 
               LStudyWork, LHearingKown, LHearingKind, LHearingImp, as.matrix(duration), 
               file="output/Exp3_demographics.txt")
rm(Age, range, sex, language, LStudyWork, LHearingKown, LHearingKind, LHearingImp, duration)


## comments
write.csv(data.frame(S3$participant, S3$VPN_Code, S3$Anmerkungen, S3$InstructionsClear, S3$Strategy), file="output/Exp3_comments.txt")


# after Exp-Evaluation
Eval1 <- table(S3$Eval1)
Eval2 <- table(S3$Eval2)
Eval3 <- table(S3$Eval3)
Eval4 <- table(S3$Eval4)
Eval5 <- table(S3$Eval5)
Eval6 <- table(S3$Eval6)
Eval7 <- table(S3$Eval7)
Eval8 <- table(S3$Eval8)

label <- c("Im Alltag achte ich stets auf den Klang der Stimme einer Person.",          # I always attend to the sound of voices in daily life
           "Es fiel mir äußerst schwer, die Stimmen zu bewerten.",                                # it was very hard to me to classify the voices
           "Bei den meisten Stimmen hatte ich gar keine Ahnung, was ich drücken sollte.",        # in many trials I had no idea which key to press
           "Ich habe die ganze Zeit aufmerksam zugehört.",                                       # I listened very attentively all the time
           "Oft habe ich einfach irgendetwas geklickt.",                                         # Most of the time I responded randomly
           "Ich fand dieses Hörexperiment äußerst interessant.",                                 # The listening experiment was very interesting.  
           "Ich habe in jedem Durchgang versucht, die Bewertung so gut wie möglich zu machen.",  # I tried my best in every trial.
           "Ich achte normalerweise gar nicht auf die Stimmen von Personen.")                    # Usually I don't listen to the sound of peoples voices at all. 


capture.output(label[1], Eval1, label[2],Eval2, label[3],Eval3,label[4], Eval4, label[5],Eval5, label[6], Eval6,label[7], Eval7, label[8], Eval8,file="output/Exp3_after_experiment_evaluation.txt")
rm(label, Eval1, Eval2, Eval3, Eval4, Eval5, Eval6, Eval7, Eval8)


